def Add(value1,value12):
    ret = value1 + value12   
    return ret

def Sub(value1,value12):
    ret = value1 - value12
    return ret

def Mult(value1,value12):
    ret = value1 * value12
    return ret

def Div(value1,value12):
    if(value12==0):
        return 0
    else:
        ret = value1/value12
        return ret
           

